-- This script creates the necessary items to dump the potential AFD synonyms

-- returns the name_usage id for the record that belngs to 
-- the specified cl and lexical group or -1
CREATE OR REPLACE FUNCTION getSynIdForChecklistLG(lg integer, cl integer) RETURNS integer AS $$
DECLARE
	ret_id integer;
BEGIN	
select id into ret_id from name_usage where checklist_fk = cl
and lexical_group_fk = lg and is_synonym;
return ret_id;
end;
$$ LANGUAGE plpgsql;

