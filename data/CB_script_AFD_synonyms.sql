--   (1) Performs authority expension of known abbreviated authornames using
--    table "auth_abbrev_test1" (must be available and populated with relevant content)
--   (2) Recognises "and", "et", "&" as equivalents (special case for "et al.") - all normalized to ampersand
--   (3) Recognises (e.g.) "Smith 1980" and "Smith, 1980" as equivalents - comma is removed in these cases
--   (4) Recognises (e.g.) "F. J. R. Taylor, 1980" and "F.J.R. Taylor, 1980" as equivalents -
--         extra space after full stops is ignored in these cases
--   (5) Returns uppercase string, diacritical marks intact 
----------------------------------
CREATE OR REPLACE function normalize_auth (str text) returns text AS $$
DECLARE
 	temp text := '';
 	this_word text := '';
 	elapsed_chars text := '';
 	this_auth_full text := '';
    bl_chars constant varchar(3) := ' ' || chr(10) || chr(13);
 begin

	if str is null or str = '' or ltrim(rtrim(str, bl_chars),bl_chars) is null then
		
		return '';
		
	else

		-- trim any leading, trailing spaces or line feeds
		temp := ltrim(rtrim(str, bl_chars),bl_chars);
		
	end if;

	if temp is null or temp = '' then
	
		return '';
		
	else
	
		-- treat some special cases here (probably more to come)
		
		if temp = 'L.' then
			temp := 'Linnaeus';
		elsif temp like '(L.)%' then
			temp := '(Linnaeus)'||substr(temp,5);
		elsif (temp like 'L., 1%' or temp like 'L. 1%') then
			temp := 'Linnaeus'||substr(temp,3);
		elsif (temp like '(L., 1%' or temp like '(L. 1%') then
			temp := '(Linnaeus'||substr(temp,4);
			
		-- more variants added 08/2009
		elsif replace(temp,'È','e') = 'Linne' then
			temp := 'Linnaeus';
		elsif replace(temp,'È','e') like '(Linne)%' then
			temp := '(Linnaeus)'||substr(temp,8);
		elsif (replace(temp,'È','e') like 'Linne, 1%' or replace(temp,'È','e') like 'Linne 1%') then
			temp := 'Linnaeus'||substr(temp,6);
		elsif (replace(temp,'È','e') like '(Linne, 1%' or replace(temp,'È','e') like '(Linne 1%') then
			temp := '(Linnaeus'||substr(temp,7);
		---
		
		elsif (temp = 'DC.' or temp = '(DC.)') then
			temp := replace(temp, 'DC.', 'de Candolle');
		elsif (temp = 'D.C.' or temp = '(D.C.)') then
			temp := replace(temp, 'D.C.', 'de Candolle');
		end if;


		-- add space after full stops, except at end (NB, will also add spece before some close brackets)
		temp := rtrim(replace(temp,'.','. '));
				
		--normalise "et", "and" to ampersand (et al. is a special case)
		if temp like '% et al%' then
			temp := replace(temp,' et al','zzzzz');
		end if;
		
		temp := replace(temp,' et ',' '||'&'||' ');
		temp := replace(temp,' and ',' '||'&'||' ');
		
		if temp like '%zzzzz%' then
			temp := replace(temp,'zzzzz',' et al');
		end if;
		
		--remove commas before dates (only)
		if temp like '%, 17%' then
			temp := replace(temp,', 17',' 17');
		end if;
		if temp like '%, 18%' then
			temp := replace(temp,', 18',' 18');
		end if;
		if temp like '%, 19%' then
			temp := replace(temp,', 19',' 19');
		end if;
		if temp like '%, 20%' then
			temp := replace(temp,', 20',' 20');
		end if;

		-- reduce multiple internal spaces to single space
		if temp like '%  %' then
			temp := reduce_spaces(temp);		
		end if;
		
		if temp like '% -%' then
			temp := replace(temp, ' -','-');		
		end if;
		
		-- expand abbreviated authors section
		
		-- snip into "words" at trailing space 
		-- add a trailing space (else will loop forever!)
		temp := temp||' ';
		
		while length(temp) > 1 loop
		
			this_word := substr(temp,1,instr(temp,' ',1)-1);
			temp := substr(temp,instr(temp,' ',1)+1);
			
			if this_word like '(%' then
			
				elapsed_chars := elapsed_chars||'(';
				this_word := substr(this_word,2);
				
			end if;

			if this_word like '%.' and length(this_word) >2 then
			
				begin

					select auth_full
					into this_auth_full
					from auth_abbreviations
					where auth_abbr = this_word
					and auth_full != '-'
					limit 1;

				exception

					when no_data_found then null;

				end;
				
				if this_auth_full is not null then
				
					this_word := this_auth_full;
					this_auth_full := null;
					
				end if;
				
			end if;
			
			elapsed_chars := elapsed_chars||this_word||' ';
					
		end loop;
		
		if elapsed_chars like '% )%' then
		
			elapsed_chars := replace(elapsed_chars,' )',')');
			
		end if;
		
		return upper(ltrim(rtrim(elapsed_chars)));

	end if;
	
end;
$$ LANGUAGE plpgsql;

--==============================================================================
-- Function: compare_auth
-- Modified to work with Postgres December 2010
-- Purpose: Compares two authority strings
-- Author: Tony Rees (Tony.Rees@csiro.au)
-- Date created: March 2008
-- Inputs: authority string 1 as auth1, authority string 1 as auth2
-- Outputs: Numeric similarity value of the 2 strings using weighted n-gram analysis,
--    on 0-1 scale (1 = identical - typically after normalization; 0 = no similarity)
-- Remarks:
--   (1) Invokes function "normalize_auth" on both strings, to compare only normalized
--         versions of the same
--   (2) Returns blend of 2/3 bigram, 1/3 trigram similarity (bigrams better correspond
--         to intuitivesimilarity, however are insensitive to word order, i.e. "Smith et
--         Jones" = "Jones et Smith" without some trigram contribution)
--   (3) Returns blend of 50% similarity with, and 50% without, stripping of diacritical
--         marks - so that the contribution of the latter is reduced but not eliminated
--   (4) Is case insensitive (i.e. "de Saedeleer" = "De Saedeleer", etc.)
--   (5) Threshold between low / possible / good match is in the area of
--         0-0.3 / 0.3-0.5 / 0.5+.
---------------------------------- 
-- Changes:
--   10/12/2010 (TR):  Added code to detect cases where mismatch is due to presence of year in one
--     term only, downgrade mismatch weight in this case
--   21/12/2010 (TR):  Added code to detect cases where mismatch is due to presence of brackets in one
--     term only, downgrade mismatch weight in this case
--==============================================================================
CREATE OR REPLACE function compare_auth(auth1 text , auth2 text ) returns numeric AS $$
DECLARE
new_auth1 text;
new_auth2 text;
new_auth1b text;
new_auth2b text;
temp_auth_match1 numeric;
temp_auth_match2 numeric;
this_auth_match numeric;

--  added 12/2010
auth1_has_date char(1) := 'N';
auth2_has_date char(1) := 'N';


begin

	if auth1 is null or auth2 is null then
	
		return null;
		
	else
	
		new_auth1 := normalize_auth(auth1);
		new_auth2 := normalize_auth(auth2);
		
		if new_auth1 = new_auth2 then
		
			this_auth_match := 1;
			
		else
		
			--  (new 12/2010): quick test for trailing dates (not exhaustive)
			--    NB, last char may be a bracket - not tested
			if substr(new_auth1,length(new_auth1)-4,1) between '0' and '9' and
			  substr(new_auth1,length(new_auth1)-3,1) between '0' and '9' and
			  substr(new_auth1,length(new_auth1)-2,1) between '0' and '9' then

				auth1_has_date := 'Y';

			end if;

			if substr(new_auth2,length(new_auth2)-4,1) between '0' and '9' and
			  substr(new_auth2,length(new_auth2)-3,1) between '0' and '9' and
			  substr(new_auth2,length(new_auth2)-2,1) between '0' and '9' then

				auth2_has_date := 'Y';

			end if;
			
			-- remove last 3 date digits to reduce discrepancy due to dates if one auth has a date
			--   and the other does not
			
			-- e.g. without this, Linnaeus <-> Linnaeus, 1758 gave a similarity of 0.7617, cf. 0.8727 with it
			--   (Linnaeus) <-> (Linnaeus, 1758) gave a similarity of 0.7237, cf. 0.8119 with it
			--   actual quantum of improvement is slightly variable according to term length, but still useful
			
			if auth1_has_date = 'Y' and auth2_has_date = 'N' then
				if new_auth1 like '%)' then
					new_auth1 := rtrim(substr(new_auth1,1, length(new_auth1)-4))||')';
				else
					new_auth1 := rtrim(substr(new_auth1,1, length(new_auth1)-3));
				end if;
			elsif auth2_has_date = 'Y' and auth1_has_date = 'N' then			
				if new_auth2 like '%)' then
					new_auth2 := rtrim(substr(new_auth2,1, length(new_auth2)-4))||')';
				else
					new_auth2 := rtrim(substr(new_auth2,1, length(new_auth2)-3));
				end if;
			end if;


			-- remove leading opening bracket to reduce discrepancy due to parentheses if one auth has parentheses
			--   and the other does not
			
			-- e.g. without this, Linnaeus, 1758 <-> (Linnaeus, 1758) gave a similarity of 0.7625, cf. 0.8773 with it
			--   actual quantum of improvement is slightly variable according to term length, but still useful
			
			if new_auth1 like '(%' and new_auth2 not like '(%' then
				new_auth1 := substr(new_auth1,2);
			elsif new_auth2 like '(%' and new_auth1 not like '(%' then
				new_auth2 := substr(new_auth2,2);
			end if;

			-- create second versions without diacritical marks
			new_auth1b := translate(new_auth1,'¡…Õ”⁄¿»Ã“Ÿ¬ Œ‘€ƒÀœ÷‹√—’≈«ÿ',
				  'AEIOUAEIOUAEIOUAEIOUANOACO');
			new_auth2b := translate(new_auth2,'¡…Õ”⁄¿»Ã“Ÿ¬ Œ‘€ƒÀœ÷‹√—’≈«ÿ',
				  'AEIOUAEIOUAEIOUAEIOUANOACO');
				  
			-- additional treatment of ligatures added 08/2009
			new_auth1b := replace(replace(new_auth1,'∆','AE'),chr(140),'OE');
			new_auth2b := replace(replace(new_auth2,'∆','AE'),chr(140),'OE');
			--
	
			-- weighted ngram comparison, use 67% n=2, 33% n=3

			-- use mean of versions with and without diacritical marks (to lessen their effect by 50%)

			temp_auth_match1 := ((2 * ngram(new_auth1,new_auth2,2))
			  + ngram(new_auth1,new_auth2,3))/3;

			temp_auth_match2 := ((2 * ngram(new_auth1b,new_auth2b,2))
			  + ngram(new_auth1b,new_auth2b,3))/3;

			this_auth_match := (temp_auth_match1 + temp_auth_match2) /2;

		end if;
		
		return round(this_auth_match,4);
	
	end if;
	
end;
$$ LANGUAGE plpgsql;


-- Creates a gender neutral version of the supplied id.
CREATE OR REPLACE FUNCTION getGenderNeutralName(id varchar) RETURNS varchar AS $$
BEGIN
	if id like '%us' then
		RETURN trim(trailing 'us' from id) || 'a';
	elsif id like '%um' then
		RETURN trim(trailing 'um' from id) || 'a';
	else
		RETURN id;
	end if;
END;
$$ LANGUAGE plpgsql;


-- Creates a "Near match" of the supplied id.  This is based on an algorithm by
-- Tony Rees.
CREATE OR REPLACE FUNCTION getNearMatchName(name_fk integer) RETURNS text AS $$
DECLARE 
canname text;
tmp text;
startletter char(1);
hasepithet boolean;
l integer;
result2 text;
next_char char(1);
BEGIN

-- construct a canonical id from the parsed id entry
select monomial || ' ' || COALESCE(specific_epithet,'') || ' ' || COALESCE(infra_specific_epithet ,'') ,
specific_epithet is not null into canname ,hasepithet
from parsed_name pn where pn.name_fk = name_fk;

-- now process the id into near matches...
tmp := UPPER(canname);

-- Do some selective replacement on the leading letter/s only:

		  if tmp like 'AE%' then
		    tmp := 'E'||substr(tmp,3);
		  elsif tmp like 'CN%' then
		    tmp := 'N'||substr(tmp,3);
		  elsif tmp like 'CT%' then
		    tmp := 'T'||substr(tmp,3);
		  elsif tmp like 'CZ%' then
		    tmp := 'C'||substr(tmp,3);
		  elsif tmp like 'DJ%' then
		    tmp := 'J'||substr(tmp,3);
		  elsif tmp like 'EA%' then
		    tmp := 'E'||substr(tmp,3);
		  elsif tmp like 'EU%' then
		    tmp := 'U'||substr(tmp,3);
		  elsif tmp like 'GN%' then
		    tmp := 'N'||substr(tmp,3);
		  elsif tmp like 'KN%' then
		    tmp := 'N'||substr(tmp,3);
		  elsif tmp like 'MC%' then
		    tmp := 'MAC'||substr(tmp,3);
		  elsif tmp like 'MN%' then
		    tmp := 'N'||substr(tmp,3);
		  elsif tmp like 'OE%' then
		    tmp := 'E'||substr(tmp,3);
		  elsif tmp like 'QU%' then
		    tmp := 'Q'||substr(tmp,3);
		    
		  -- additional case added 06/2009
		  elsif tmp like 'PH%' then
		    tmp := 'F'||substr(tmp,3);
		  --
		  
		  elsif tmp like 'PS%' then
		    tmp := 'S'||substr(tmp,3);
		  elsif tmp like 'PT%' then
		    tmp := 'T'||substr(tmp,3);
		  elsif tmp like 'TS%' then
		    tmp := 'S'||substr(tmp,3);
		  elsif tmp like 'X%' then
		    tmp := 'Z'||substr(tmp,2);
		  end if;
		  
-- Now keep the leading character, then do selected "soundalike" replacements. The 
		-- following letters are equated: AE, OE, E, U, Y and I; IA and A are equated;
		-- K and C;  Z and S; and H is dropped. Also, A and O are equated, MAC and MC are equated, and SC and S.

		  startletter :=substr(tmp,1,1);  -- quarantine the leading letter
		  tmp := substr(tmp,2);  -- snip off the leading letter


		
		  -- now do the replacements

		  tmp := replace(tmp, 'AE', 'I');
		  tmp := replace(tmp, 'IA', 'A');
		  tmp := replace(tmp, 'OE', 'I');
		  tmp := replace(tmp, 'OI', 'A');
		  tmp := replace(tmp, 'SC', 'S');
		  tmp := translate(tmp, 'EOUYKZH', 'IAIICS');

		  --add back the leading letter
		  tmp := startletter||tmp;





l := length(tmp);

		  for i in 1..l loop
		     next_char := substr(tmp, i, 1);
		     if i = 1 then
		       result2 := next_char;
		     elsif next_char = substr(result2, length(result2),1) then
 		       result2 := result2;
		     else
		       result2 := result2||next_char;
		     end if;
		   end loop;
		   
		   if length(result2) >4 and hasepithet then

			   -- deal with variant endings -is (includes -us, -ys, -es), -im (was -um), -as (-os)
			   -- at end of string or word: translate all to -a
			   result2 := result2||' ';

			   if result2 like '%IS %' then
				result2 := replace(result2, 'IS ','A ');
			   end if;
			   if result2 like '%IM %' then
				result2 := replace(result2, 'IM ','A ');
			   end if;
			   if result2 like '%AS %' then
				result2 := replace(result2, 'AS ','A ');
			   end if;

			   -- strip off last space again
			   result2 := rtrim(result2);

		    end if;

		    return result2;

END;
$$ LANGUAGE plpgsql;


